package com.cmdi.model;

public class GroupInfo {
    private String id;

    private String name;

    private String region;

    private String contactPerson;

    private String phoneNumber;

    private String email;

    private String addTime;

    private Integer status;

    private String personAddress;

    private String personAddressQr;

    private String resultAddress;

    private String resultAddressQr;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getContactPerson() {
        return contactPerson;
    }

    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddTime() {
        return addTime;
    }

    public void setAddTime(String addTime) {
        this.addTime = addTime;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getPersonAddress() {
        return personAddress;
    }

    public void setPersonAddress(String personAddress) {
        this.personAddress = personAddress;
    }

    public String getPersonAddressQr() {
        return personAddressQr;
    }

    public void setPersonAddressQr(String personAddressQr) {
        this.personAddressQr = personAddressQr;
    }

    public String getResultAddress() {
        return resultAddress;
    }

    public void setResultAddress(String resultAddress) {
        this.resultAddress = resultAddress;
    }

    public String getResultAddressQr() {
        return resultAddressQr;
    }

    public void setResultAddressQr(String resultAddressQr) {
        this.resultAddressQr = resultAddressQr;
    }
}